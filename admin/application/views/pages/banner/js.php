tinymce.init({
  selector: '.desc',
  height: 300,
  menubar: true,
  theme: 'modern',
  plugins: [
    'advlist autolink lists link image charmap print preview anchor',
    'searchreplace visualblocks code fullscreen',
    'insertdatetime media table contextmenu paste code'
  ],
